#include "push_swap.h"

long	ft_atol(const char *s)
{
	long	n;
	int		sign;

	n = 0;
	sign = 1;
	if (*s == '-' || *s == '+')
		if (*s++ == '-')
			sign = -1;
	while (*s)
	{
		n = n * 10 + (*s - '0');
		s++;
	}
	return (n * sign);
}
